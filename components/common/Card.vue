<script setup lang="ts">
import type { TImage, TRecipe } from '~/types/recipe';

const props = defineProps<{
  card: TRecipe,
  to: string,
}>()

const src = computed(() => {
  const { images } = props.card
  if (images && images.length) {
    const mainImage: TImage = images[0]
    if ('url' in mainImage) {
      return mainImage.url
    }
  }
})

</script>

<template>
  <div class="card">
    <div class="card__actions">

      <button type="button" class="card__btn" @click="$router.push(`/recipe/${card.id}/edit`)">
        <IconsIconEdit />
      </button>
    </div>
    <NuxtLink :to="to" class="card__link">

      <div class="card__image" :style="{ backgroundImage: `url(${src})` }">
      </div>
      <div class="card__description">
        <h3 class="card__title" :title="card.title">{{ card.title }}</h3>
        <div class="card__tags">
          <CommonVTag v-for="tag in card.tags" :key="tag.title" :label="tag.title" :tag="tag" inactive filled />
        </div>
      </div>
    </NuxtLink>
  </div>
</template>

